/**
Refine the getFirstCharacter function to return the first character of the name parameter provided to it.
 */
let a = "Osaka";
let b = "Kyoto";
let c = "Kobe";

function getFirstCharacter(a) {
  return a.charAt(a);
}

console.log(getFirstCharacter(a));
console.log(getFirstCharacter(b));
console.log(getFirstCharacter(c));
