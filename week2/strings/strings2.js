/**
Refine the shoutMyName function to return the name parameter it receives, converted entirely to uppercase.
 */

let a = "Arina";
let b = "Lily";
let c = "Nanao";

function shoutMyName(a) {
  return a.toUpperCase(a);
}

console.log(shoutMyName(a));
console.log(shoutMyName(b));
console.log(shoutMyName(c));
